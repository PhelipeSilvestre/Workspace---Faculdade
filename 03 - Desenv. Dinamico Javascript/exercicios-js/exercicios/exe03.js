/*crie uma função que receba a quantidade de horas trabalhadas no mes
e o valor das horas trabalhadas e retorne o salario no mesmo.
 */

function calcularSalario(horasTrabalhadas, valorHoras) {
    return horasTrabalhadas * valorHoras
}

